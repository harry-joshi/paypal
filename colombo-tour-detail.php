                                                                                                                                                    <p><span class="daybold">Day 1</span>-&nbsp; Arrive in Negombo
You are met at the airport and taken 20 minutes by car to Negombo, a fishing port and coastal resort. After you settle in at the hotel, time for some beach- or pool-based R & R. From the shore, you can admire the flotilla of oruwas, traditional fishing boats whose square sails dance on the ocean.
</p>
<p><span class="daybold">Day 2</span>-&nbsp;  Negombo - Kalpitiya - 68 miles
We begin the tour by heading north towards Kalpitiya peninsula, along a series of minor coastal roads and some tracks, passing through fishing villages. We reach a stretch lined by huge coconut plantations before they give way to shrimp farms. We fork off onto the peninsula and ride between the lagoon and the ocean.
</p>
<p><span class="daybold">Day 3</span>-&nbsp;  Kalpitiya - Sigiriya - 118 miles
After around 18 miles of main road skirting the immense Karuwalagas lagoon, we cross a plain peppered with jungles, rice paddies, coconut plantations, mango groves and several lakes. We make a detour to visit the Ritigala ruins, a monastery that was abandoned many years ago, nestled in the heart of the jungle. Only when the imposing Lion Rock is dominating the reddish plains do we know that we’ve arrived in Sigiriya.
</p>
<p><span class="daybold">Day 4</span>-&nbsp;  Sigiriya - 84 miles
Today, we have two options: we can visit either the ancient city of Polonnaruwa or Dambulla’s Buddhist cave temple. We ride along the edge of Minneriya National Park, where elephant sightings are common. After a break near Giritale tank, we head to Polonnaruwa, a UNESCO World Heritage Site. Alternatively, we can head to Dambulla and its astounding caves housing numerous Buddha statues.
</p>
<p><span class="daybold">Day 5</span>-&nbsp; Sigiriya - Trincomalee - 99 miles
This morning, we visit the Lion Rock, a UNESCO World Heritage Site and hike to the top, where we’re rewarded with spectacular views. We head northeast along indescribably charming country roads for around 12 miles, then rough and ready tracks to end on a recently renovated, easy road straight to the coast. We stop in Trincomalee, a city that welcomed the end of the Tamil Tiger conflict.
</p>
<p><span class="daybold">Day 6</span>-&nbsp;  Trincomalee - Wasgamuwa - 134 miles
We drive south through the endless eastern plains, peppered with pristine expanses, enormous lakes and protected natural parks. We enjoy a long, wide track, which resembles an off-road highway. This part of the island is uncrowded and this leg takes us to the foothills of a large mountain range in south-central Sri Lanka. We stop at the foot of Knuckles mountain range.
</p>
<p><span class="daybold">Day 7</span>-&nbsp;  Wasgamuwa - Knuckles - 62 miles
We drive alongside the Knuckles range, a beautiful, tall mountain that remains very wild. We enjoy magnificent views of reservoirs with deep blue waters and the mountain range getting closer and closer until we reach a smooth, well-designed road that takes up into the cool high-altitude tropical rainforests above 1,000 m altitude. Finally, we end this beautiful leg passing our first tea plantations and rural villages.
</p>
<p><span class="daybold">Day 8</span>-&nbsp; Knuckles - Nuwara Eliya - 78 miles
We continue our adventure, venturing into the heart of the island and its highlands, overlooked by the country’s highest peaks. We climb up to almost 2,000 metres. The road winds through several tea plantations and their artistic formations. When we reach Nuwara Eliya, the country’s highest city, you’ll soon realise that its nickname “Little England” is no exaggeration: it still carries a strong British influence.
</p>
<p><span class="daybold">Day 9</span>-&nbsp;  Nuwara Eliya - Ella - 56 miles
We are in the heart of Sri Lanka's tea country. We take a deliberately arduous route so that we can really make the most of this unique region. As well as tea bushes, we also pass many dairy farms, a landscape that could easily be passed off as Switzerland. We arrive in Ella for lunch, a peaceful refuge for many travellers. We then ride to our stop for the night, just outside this small town.
</p>
<p><span class="daybold">Day 10</span>-&nbsp;  Ella - Udawalawe - 99 miles
Today, we leave the central highlands behind us and head for the south coast. We make one last tea-related visit to the historical Sir Lipton’s tea factory, and weather permitting, we may even be able to take a ride to Lipton’s Seat, a lookout with perfect views across this empire of green gold. We take a break at Diyaluma Falls, the third highest in Sri Lanka. We stay on the edge of the famous Udawalawe national park.
</p>
<p><span class="daybold">Day 11</span>-&nbsp; Udawalawe - Galle - 78 miles
We enjoy an early morning safari in Udawalawe national park, where elephant sightings are guaranteed. Then, we head to the much-loved south coast. En route, we stop off at Mulkirigala rock temples and the reclining Buddhas in the caves, proof of the island’s ancient Buddhist culture. We reach the turquoise waters of the coast, take a break next to the sea and then ride on to Galle, an old Dutch fortified city.
</p>
<p>
    <span class="daybold">Day 12</span>-&nbsp;  Galle - Colombo international airport
Today, we ride to Colombo. You are then transferred to the airport in a private vehicle based on your flight time. Important: Make sure that you book an evening flight.

</p>


