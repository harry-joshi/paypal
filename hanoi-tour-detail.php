                                                                                                                                                    <p><span class="daybold">Day 1</span>-&nbsp; HANOI ARRIVAL
Introduce yourself to our driver who’ll be holding a sign with your name in the airport Arrivals lounge. The journey to the Hanoi Club Hotel takes about an hour prepare yourself for an entertaining and sometimes hair-raising experience full of chaotic traffic, hectic architecture, overloaded motorcycles and noise. Settle into your hotel and explore Hanoi city your own way, perhaps taking a half-day Hanoi City Tour (inexpensive optional extra) and tasting a bia hoi, Vietnams own draught beer or microbrew. In the evening gather for a welcome dinner and pre-trip briefing.
</p>
<p><span class="daybold">Day 2</span>-&nbsp; HANOI TO BA BE
Depart Hanoi by bus at 9am to avoid the traffic. Travel about 120km on the cars-only highway to Thai Nguyen and get ready for the real adventure to begin. After lunch collect our bikes. Todays 150km ride takes us off the regular tourist route into Be Ba National Park through massive limestone mountains and plunging valleys dotted with waterfalls and caves, and wrapped in flourishing rainforest vegetation. Gape at the spectacular scenery around Ba Be, Vietnam?s largest natural lake. Enjoy overnight hospitality in a stilt house homestay with Tay minority people in the tribal village.
</p>
<p><span class="daybold">Day 3</span>-&nbsp; BA BE TO CAO BANG
Welcome the day with a homestay breakfast. It’s an easy wander to the Ba Be’s shoreline to board a traditional longboat excursion to fully appreciate the tranquility of this lush and remote natural paradise. Return to the homestay, pack and jump on the bikes. Lunch is en route today as we ride almost 200km through lush greenery and small villages on mountain roads to the capital of Cao Bang Province. This area has a history of revolution and turbulence, and legends of heroism dating back to the third century. Take in 360 views from the hill-summit war memorial, and visit historical caves and the thundering Ban Gioc Waterfall. Overnight in Cao Bang.
</p>
<p><span class="daybold">Day 4</span>-&nbsp; CAO BANG TO MEO VAC
Leaving the Cao Bang capital behind head northwest 200km through some seriously gorgeous karst (towering rocky outcrop) scenery that provides a sensational contrast to the regions dense farmland vegetation. Ethnic minorities including the Hmong, King, Tay and Dzao have maintained their culture in virtual isolation untouched by the outside world for centuries. Life is simple in this is truly iconic Vietnamese countryside. Sense our closeness to the Chinese border on passing the gigantic Ho Chi Minh statue and a series of communist-inspired billboards, which highlight a culture of ambition, ruthlessness and struggle. Alcoholic home brews are popular here with bee wine a mysterious and potent local. Expect cooler temperatures.
</p>
<p><span class="daybold">Day 5</span>-&nbsp; MEO VAC TO HA GIANG
Rise at dawn for an immensely thrilling ride through Ma Pi Leng, Vietnams most breath- taking pass, which winds for 22 km cut from the side of black limestone mountains. The jaw- dropping scenery includes rippling hills, which descend to the distant Nho Que River. Enjoy a local breakfast of pho in Dong Van, browse the sights and partake excellent coffee from charming cafes. Today we ride 150km on winding roads through the harsh, but stunningly beautiful, mountain landscape where hardy minority families diligently farm unyielding lands. One of the norths greatest road trips will then take you down plummeting valleys past cool river cuttings and fragrant crop plantings.
</p>
<p><span class="daybold">Day 6</span>-&nbsp; HA GIANG TO BAC HA
Today requires an early start for the long and exciting 200km journey ahead. The route will be determined by current condition of the road and weather forecasts. This is exhilarating riding with the region being prone to landslides, narrow passes and challenging frequent road works. Ride the winding saddle to the lookout and see why this vista of knobbly-topped mountains is a UNESCO-classified Geopark. Stop for meals and to photograph the awe- inspiring surrounds, which highlight the vast beauty of the environment. Bac Ha is a rural district of Lao Cai Province in Vietnams northeast. Its home to six groups of Hmong, one of the 54 minorities of Vietnam.
</p>
<p><span class="daybold">Day 7</span>-&nbsp; BAC HA TO SAPA
An easy ride today, stopping for coffee in Lai Cai, where China is visible across the Song Hong River. Then its a fun 60km rumble up a steep and winding road to the mountain retreat of Sapa, a former French hill station and Vietnam?s premier trekking base. The temperatures much cooler and thick rolling mist often shrouds the peaks. Experience the vivid colour and energy of the traditional hill-tribe costumes as locals set up to sell craftwork in this town that teems with tourists. Witness the flurry of activity as Sapas construction boom gains momentum. We ride a total of 110km today alongside cascading rice terraces and scattered hill-tribe villages.
</p>
<p><span class="daybold">Day 8</span>-&nbsp; SAPA TO LAI CHAU
Free morning to relax or explore Sapas sights and enjoy cooler temperatures. Check out of the hotel then hit the road after lunch for a relaxing ride down into the picturesque valley. View the never-ending rice field terraces spread like a giant patchwork quilt on the undulating hills that merge then vanish beyond the horizon. Thread your Bullet through the twists and turns of the road for an experience in meandering mindfulness and sheer and simple joy. There’s plenty of time for photos of industrious locals going about their work and private lives. After 120km of motorcycling bliss check into Lai Chau accommodation.
</p>
<p><span class="daybold">Day 9</span>-&nbsp; LAI CHAU TO DIEN BIEN PHU
Today we head to the southwest corner of Northern Vietnam and close to the border with Laos. Ride the areas towering mountains after breakfast, enjoy good quality roads (created when the Old Town was flooded and relocated 3km to the south-east), and sense outstanding serenity and tranquility for the 210km journey south. The scenery is stunning with the city built on a broad plain and surrounded by dramatic peaks and thick, almost impenetrable forested peaks. Overnight in Dien Bien Phu where, in 1954, the Viet Minh remarkably defeated the French after a 57-day siege. View an eclectic collection that commemorates this battle at the war museum.
</p>
<p><span class="daybold">Day 10</span>-&nbsp; DIEN BIEN PHU TO SON LA
There’s time to climb to the top of A1 Hill, the site of Vietnams victory against the French, before we hit the road today. View well-preserved trenches, old tanks and monuments. Enjoy a panoramic view of the town, and contemplate the significance of this remarkable victory that quelled French attempts to re-establish colonial control. Ride the intrepid mountain passes and soak in the profound peace and tranquility along our 180km journey. Eighty percent of Son La Province is covered with soaring mountain pinnacles with dizzying ascents. Pass lush paddy fields, great roads and local villages where the Xa, Hmong Dao, Muang, Kinh Thay and Thai ethnic groups live.
</p>
<p><span class="daybold">Day 11</span>-&nbsp; SON LA TO MAI CHAU
Todays 200km ride takes us to an isolated haven of minority hill tribes nestled between towering cliffs in an idyllic rural valley of lush greenery and terraced fields of rice paddies. Feel one with your bike as you weave your way through the soothing landscape. This is one of Vietnams most ethnically diverse regions with many village tribes creating fabric accessories and jewellery to sell from road-side stalls to tourists. This evening we enjoy the hospitality of the Thai minority in a traditional stilt home. A great opportunity to sample some local rice wine with the homeowners … to decline their hospitality would be impolite!
</p>
<p><span class="daybold">Day 12</span>-&nbsp; MAI CHAU TO HANOI
Sleep off a rice-wine induced foggy head, farewell our hosts and begin our 50km journey with an adrenaline-pumping ride up the mountain and through a stunning scenic pass. The villagers are mostly White Thai who no longer wear traditional costume. The women create traditional textiles, garments and housewares. Once down the mountain we ride a further 100km soaking in the pleasure of adventure on two wheels on a superbly engineered and carefully handcrafted machine. Before we reach Hanoi we load the bikes on the truck and board the bus where we can relax for the remainder of the journey into Hanoi.
</p>
<p><span class="daybold">Day 13</span>-&nbsp; HANOI TO HA LONG BAY
Early morning shuttle bus pick-up for Tuan Chau International Harbour to embark on the LaFairy 2-day cruise in Ha Long Bay. Leave your main luggage at the hotel bringing only what you need for relaxation. Enjoy a welcome drink and sumptuous meals as the boat meanders through the extraordinarily beautiful limestone islands considered by some a Heaven on Earth. This expansive and majestic location is dotted with tranquil lagoons and enchanting grottos. View breathtaking vistas and visit awe-inspiring Me Cung Cave. Enjoy party drinks at sunset, marvel at fruit carving, watch traditional music performances and dine on the top deck under the stars. Maybe even try your hand at night squid fishing. (Pay for your own drinks and incidental expenses on board).
</p>
<p><span class="daybold">Day 14</span>-&nbsp; HA LONG BAY TO HANOI
Our second day on La Fairy Sales is planned to delight. Start the new day with a Tai Chi session or photograph the Bay in the mornings alluring light. Visit Ti Tov Island for swimming in emerald waters or trek to the top of the island for another breathtaking panoramic view of this exquisite area. Kayak through the hidden beauty of the Bay or attend a cooking demonstration of visually vibrant and fragrantly fresh local dishes before an exceptional buffet lunch. Disembark after farewells, and take the shuttle bus back to Hanoi hotel. Spend remainder of day at leisure, resting or shopping for last minute bargains and experiences.
</p>
<p><span class="daybold">Day 15</span>-&nbsp; HANOI TO AIRPORT TRANSFER
Free time at your leisure then transfer to international airport of return trip home.
</p>
